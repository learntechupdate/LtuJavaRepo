package javaconcepts;

public class AnamingConvention2 {

	/*
	 interface - Adjective(able) eg: Runable,Readable, Remote, Serializable,
	 Class Name - Noun eg: Student, Person, 
	 Method : Verb eg . actionPreformed, run.print, write
	 Variable eg :stockPrice, bankDetails
	 Constant eg. PI, DENSITY, MAX_PRICE	
	 
	    interface,ClassName : 1st Letter should be Cap
	    method, variable, package - 1st letter should be lower case
	  Note : 
	  for words java uses Camel Casing Rule Ex. MyFirstJava 
	  for constant all caps seprate by _
	  for package all lower case
	  
	  Advantage : code readable 
	  ex :
	  variable : abc
	  Constant : ABC
	  constructor   : Abc()
	  method : abc()
	  
	  Interface : Stretchable  	  
	  
	 */
  	
}
